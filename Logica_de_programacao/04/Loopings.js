// Looping for
for (let i = 1; i <= 5; i++) {
  console.log(`Executando ${i} vezes com for`);
}

console.log("-----------------------------");

let n1 = 0;

while (n1 < 5) {
  console.log(`Executando ${n1} vezes com while`);
  n1++;
}

console.log("------------------------------------");

let n2 = 0;

do {
  console.log(`Executando ${n2} vezes com do-while`);
  n2++;
} while (n2 < 5);
