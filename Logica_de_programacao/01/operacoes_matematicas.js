function soma(n1, n2) {
  return n1 + n2;
}

function subtrair(n1, n2) {
  return n1 - n2;
}

function mult(n1, n2) {
  return n1 * n2;
}

function div(n1, n2) {
  return n1 / n2;
}

console.log(soma(2, 2));
console.log(subtrair(2, 2));
console.log(mult(2, 2));
console.log(div(2, 2));
